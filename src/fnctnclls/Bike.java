package fnctnclls;

public class Bike {
public static void main(String[] args) {
		
		Bike b=new Bike();
		b.add();
		b.sub();
		b.mux();
}
	

	public void add() {
		int a=10;
		int b=20;
		System.out.println(a+b);
	}
		public void sub() {
			int a=10;
			int b=20;
			System.out.println(b-a);
		} 
	public void mux() {
		int a=10;
		int b=10;
		System.out.println(a*b);
	}
}



	
	
	
	

